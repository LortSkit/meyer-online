export function translateHome(isDanish: boolean): string {
  return isDanish ? "Hjem" : "Home";
}

export function translateCreate(isDanish: boolean): string {
  return isDanish ? "Opret et spil" : "Create a game";
}

export function translateFind(isDanish: boolean): string {
  return isDanish ? "Find spil" : "Find games";
}

export function translateRules(isDanish: boolean): string {
  return isDanish ? "Regler" : "Rules";
}
